interface ProductIconProps {
  product: Product;
  className?: string;
  size?: number;
}

export function ProductIcon({ product, className, size = 20 }: ProductIconProps) {
  const icon = PRODUCT_ICONS[product];
  const [failed, setFailed] = useState(false);

  if (!icon) return null;

  if (failed) {
    return (
      <div
        className={cn(
          "rounded flex items-center justify-center text-white font-bold shrink-0",
          className
        )}
        style={{
          width: size,
          height: size,
          backgroundColor: icon.color,
          fontSize: size * 0.4,
          lineHeight: 1,
        }}
      >
        {icon.label.charAt(0)}
      </div>
    );
  }

  return (
    <svg
      width={size}
      height={size}
      viewBox={icon.viewBox}
      xmlns="http://www.w3.org/2000/svg"
      className={cn("shrink-0", className)}
      role="img"
      aria-label={icon.label}
      onError={() => setFailed(true)}
    >
      <path d={icon.path} fill={icon.color} />
    </svg>
  );
}
